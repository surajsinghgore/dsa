public class shiftOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        int c = 4;
        int d = 6;
        System.out.println("a>>1");
        System.out.println(a>>1);
        System.out.println("b>>2");
        System.out.println(b>>2);
        System.out.println("c<<1");
        System.out.println(c<<1);
        System.out.println("d<<2");
        System.out.println(d<<2);
    }
}
